				<p>Cette série d’atelier vise à faire décourvrir le fonctionnement d’internet et rendre accessible l’auto-hébergement grâce à <a href="https://labriqueinter.net">La brique internet</a>. Il s’agit d’un petit ordinateur à installer chez soi pour se connecter au réseau de façon sécurisée et gérer ses propres services d’email, de réseau social, de cloud, de partage multimédia, etc.</p>
